
public class Auction {
	
private static int itemid;

private static String seller;


private static String startTime;

private static String endTime;

private static String itemName;

private static String itemDescription;

private static String itemCategory;




//Setter and Getters
public static int getItemid() {
	return itemid;
}

public static void setItemid(int itemid) {
	Auction.itemid = itemid;
}

public static String getSeller() {
	return seller;
}

public static void setSeller(String seller) {
	Auction.seller = seller;
}

public static String getStartTime() {
	return startTime;
}

public static void setStartTime(String startTime) {
	Auction.startTime = startTime;
}

public static String getEndTime() {
	return endTime;
}

public static void setEndTime(String endTime) {
	Auction.endTime = endTime;
}

public static String getItemName() {
	return itemName;
}

public static void setItemName(String itemName) {
	Auction.itemName = itemName;
}

public static String getItemDescription() {
	return itemDescription;
}

public static void setItemDescription(String itemDescription) {
	Auction.itemDescription = itemDescription;
}

public static String getItemCategory() {
	return itemCategory;
}

public static void setItemCategory(String itemCategory) {
	Auction.itemCategory = itemCategory;
}

}